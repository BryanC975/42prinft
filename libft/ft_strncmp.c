#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	unsigned char	*str1;
	unsigned char	*str2;
	size_t			i;

	str1 = (unsigned char *)s1;
	str2 = (unsigned char *)s2;
	i = 0;
	while (str1[i] && str2[i] && (i < n - 1) && str1[i] == str2[i])
		i++;
	if (!(n <= 0))
		return (str1[i] - str2[i]);
	else
		return (0);
}
